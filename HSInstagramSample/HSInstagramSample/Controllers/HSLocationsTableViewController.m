#import "HSLocationsTableViewController.h"
#import "HSLocationService.h"
#import "HSInstagramLocation.h"
#import "HSMediaGridViewController.h"

@implementation HSLocationsTableViewController

@synthesize locations = _locations;
@synthesize locationService = _locationService;

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
        self.locationService = [[HSLocationService alloc] init];
        [self.locationService start];
    }
    return self;
}

- (void)didReceiveMemoryWarning
{
    // Releases the view if it doesn't have a superview.
    [super didReceiveMemoryWarning];
    
    // Release any cached data, images, etc that aren't in use.
